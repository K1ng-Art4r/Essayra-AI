def build_task_specific_note(topic_code: str) -> str:
    if topic_code == "13.1":
        return """
Дополнительное уточнение для задания 13.1:
- ответ должен быть именно на лингвистический вопрос темы;
- если тема спрашивает о средствах языка, экзаменуемый должен назвать релевантные средства языка / выразительные средства и объяснить их роль;
- примеры из текста засчитываются только тогда, когда они действительно подтверждают рассуждение и связаны с вопросом темы.
""".strip()

    if topic_code == "13.2":
        return """
Дополнительное уточнение для задания 13.2:
- экзаменуемый должен объяснить смысл данного фрагмента / высказывания;
- объяснение должно опираться на понимание всего текста в целом, но не выходить за его рамки;
- примеры должны подтверждать именно данное объяснение.
""".strip()

    if topic_code == "13.3":
        return """
Дополнительное уточнение для задания 13.3:
- экзаменуемый должен дать ответ на тематический вопрос, сформулированный в теме;
- рассуждение должно строиться с опорой на исходный текст;
- примеры из текста должны подтверждать тезис экзаменуемого.
""".strip()

    return ""


def build_evaluation_prompt(
    topic_title: str,
    topic_code: str,
    source_text: str,
    instructions: str,
    essay_text: str,
    word_count: int,
) -> str:
    task_note = build_task_specific_note(topic_code)

    return f"""
Ты — строгий и внимательный эксперт ОГЭ по русскому языку.
Ты проверяешь сочинение-рассуждение задания {topic_code}.

Проверяй работу строго по официальной логике ОГЭ-2026:
- для 13.1, 13.2 и 13.3 используются единые критерии содержания SK1-SK4;
- различается только сам вопрос темы;
- не завышай баллы;
- если есть сомнение между двумя оценками, выбирай меньшую;
- возвращай только валидный JSON и ничего вне JSON.

====================
ДАННЫЕ ДЛЯ ПРОВЕРКИ
====================

Код задания:
{topic_code}

Формулировка выбранной темы:
"{topic_title}"

Инструкция к заданию:
\"\"\"
{instructions}
\"\"\"

Исходный текст:
\"\"\"
{source_text}
\"\"\"

Сочинение ученика:
\"\"\"
{essay_text}
\"\"\"

Предварительно вычисленное количество слов в сочинении: {word_count}
Используй именно это число в поле wordCount.

{task_note}

========================================
ОБЯЗАТЕЛЬНЫЕ ПРЕДВАРИТЕЛЬНЫЕ ПРОВЕРКИ
========================================

Перед выставлением баллов обязательно:
1. Проверь, отвечает ли сочинение именно на вопрос, сформулированный в теме.
2. Проверь, есть ли реальная опора на исходный текст.
3. Проверь, не является ли сочинение полностью переписанным или пересказанным исходным текстом.
4. Определи количество:
   - логических ошибок;
   - орфографических ошибок;
   - пунктуационных ошибок;
   - грамматических ошибок;
   - речевых ошибок;
   - фактических ошибок.
5. Проверь, есть ли трёхчастная композиция:
   - тезис / вступление;
   - доказательства / основная часть;
   - вывод.

========================================
СПЕЦИАЛЬНЫЕ ПРАВИЛА
========================================

1. Если работа написана без опоры на исходный текст, то работа не оценивается.
   В этом случае:
   - notGraded = true
   - notGradedReason = "Работа не оценивается, так как написана без опоры на прочитанный текст."
   - totalScore = 0
   - по всем критериям выставь 0

2. Если сочинение полностью переписано или пересказано, то:
   - SK1 = 0
   - SK2 = 0
   - SK3 = 0
   - SK4 = 0

3. Если сочинение написано не на тот вопрос, который сформулирован в теме, то:
   - SK1 = 0
   - SK2 = 0
   - SK3 = 0
   - SK4 = 0

4. Если в сочинении менее 70 слов, то:
   - SK1 = 0
   - SK2 = 0
   - SK3 = 0
   - SK4 = 0

5. Этот тренажёр проверяет только сочинение, без изложения.
   Поэтому критерии GK1-GK4 и FK1 оценивай только если wordCount >= 140.
   Если wordCount <= 139, то:
   - GK1 = 0
   - GK2 = 0
   - GK3 = 0
   - GK4 = 0
   - FK1 = 0
   И в explanation по этим критериям прямо укажи, что по официальному правилу
   для одной творческой работы объём должен быть не менее 140 слов.

========================================
КРИТЕРИИ СОДЕРЖАНИЯ
========================================

SK1 — Наличие ответа на вопрос
1 балл:
- экзаменуемый дал ответ на вопрос, сформулированный в теме сочинения.

0 баллов:
- экзаменуемый не ответил на вопрос;
- или ответил неправильно.

В explanation по SK1 укажи:
- есть ли ответ на вопрос темы;
- корректный ли он.

SK2 — Наличие примеров
3 балла:
- экзаменуемый привёл два примера из прочитанного текста, подтверждающих его рассуждения.

2 балла:
- экзаменуемый привёл один пример из прочитанного текста, подтверждающий его рассуждения.

1 балл:
- экзаменуемый привёл пример(ы) из жизненного или читательского опыта, подтверждающий(-ие) его рассуждения.

0 баллов:
- экзаменуемый не привёл ни одного примера, подтверждающего его рассуждения.

Правила для SK2:
- если есть только один пример из данного текста, это 2 балла;
- если есть только пример(ы) из жизненного или читательского опыта и нет примеров из исходного текста, это 1 балл;
- если примеры формально названы, но не подтверждают тезис, не засчитывай их;
- если пример искажает текст или относится не к данному тексту, не засчитывай его как пример из исходного текста.

В explanation по SK2 укажи:
- сколько засчитанных примеров найдено;
- откуда они взяты;
- подтверждают ли они рассуждение.

SK3 — Логичность речи
2 балла:
- логические ошибки отсутствуют.

1 балл:
- допущены одна-две логические ошибки.

0 баллов:
- допущено три логические ошибки или более.

В explanation по SK3 укажи:
- сколько логических ошибок найдено;
- в чём они состоят.

SK4 — Композиционная стройность
1 балл:
- работа характеризуется трёхчастной композицией;
- ошибки в построении текста отсутствуют.

0 баллов:
- в работе нарушена трёхчастная композиция;
- или в построении текста допущена одна ошибка или более.

В explanation по SK4 укажи:
- есть ли трёхчастная композиция;
- есть ли ошибки в построении текста.

========================================
КРИТЕРИИ ГРАМОТНОСТИ И ТОЧНОСТИ
========================================

GK1 — Соблюдение орфографических норм
3 балла: орфографических ошибок нет.
2 балла: допущены одна-две орфографические ошибки.
1 балл: допущены три-четыре орфографические ошибки.
0 баллов: допущено пять орфографических ошибок или более.

GK2 — Соблюдение пунктуационных норм
3 балла: пунктуационных ошибок нет.
2 балла: допущены одна-две пунктуационные ошибки.
1 балл: допущены три-четыре пунктуационные ошибки.
0 баллов: допущено пять пунктуационных ошибок или более.

GK3 — Соблюдение грамматических норм
3 балла: грамматических ошибок нет.
2 балла: допущены одна-две грамматические ошибки.
1 балл: допущены три-четыре грамматические ошибки.
0 баллов: допущено пять грамматических ошибок или более.

GK4 — Соблюдение речевых норм
3 балла: речевых ошибок нет.
2 балла: допущены одна-две речевые ошибки.
1 балл: допущены три-четыре речевые ошибки.
0 баллов: допущено пять речевых ошибок или более.

FK1 — Фактическая точность речи
1 балл: фактические ошибки отсутствуют.
0 баллов: допущена одна фактическая ошибка или более.

Для GK1-GK4 и FK1:
- в explanation обязательно укажи количество ошибок;
- если критерий обнулён из-за объёма менее 140 слов, прямо напиши это.

========================================
ПОДСЧЁТ ИТОГА
========================================

totalScore = SK1 + SK2 + SK3 + SK4 + GK1 + GK2 + GK3 + GK4 + FK1

Максимальный балл = 20.

Если notGraded = true, то:
- totalScore = 0
- все критерии = 0

========================================
ФОРМАТ ОТВЕТА
========================================

Верни только валидный JSON по этой структуре:

{{
  "wordCount": 0,
  "notGraded": false,
  "notGradedReason": "",
  "totalScore": 0,
  "criteria": {{
    "SK1": {{"score": 0, "explanation": ""}},
    "SK2": {{"score": 0, "explanation": ""}},
    "SK3": {{"score": 0, "explanation": ""}},
    "SK4": {{"score": 0, "explanation": ""}},
    "GK1": {{"score": 0, "explanation": ""}},
    "GK2": {{"score": 0, "explanation": ""}},
    "GK3": {{"score": 0, "explanation": ""}},
    "GK4": {{"score": 0, "explanation": ""}},
    "FK1": {{"score": 0, "explanation": ""}}
  }},
  "overallComment": ""
}}

Требования к overallComment:
- 3-6 предложений;
- кратко перечисли сильные и слабые стороны;
- если работа не оценивается, прямо напиши это;
- не пиши ничего вне JSON.
""".strip()